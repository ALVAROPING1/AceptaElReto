#include <stdio.h>

inline void fastscan(int* number) {
    register int c;
    *number = 0;
    // Extract current character from buffer
    c = getchar_unlocked();
    // Keep on extracting characters if they are integers
    for (; (c >= '0' && c <= '9'); c = getchar_unlocked())
        *number = *number * 10 + c - '0';
}

