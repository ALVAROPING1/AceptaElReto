inline void insertion_sort(char arr[], int size) {
    int i, j;
    for (i = 1; i < size; ++i) {
        int temp = arr[i];
        j = i;
        while (j > 0 && arr[j - 1] > temp) {
            arr[j] = arr[j - 1];
            --j;
        }
        arr[j] = temp;
    }
}

inline int iterate(int n) {
    char buffer[4];
    register int i = 4;
    do {
        buffer[--i] = n % 10;
        n /= 10;
    } while (i);
    insertion_sort(buffer, 4);
    return (buffer[3] * 1000 + buffer[2] * 100 + buffer[1] * 10 + buffer[0]) -
           (buffer[0] * 1000 + buffer[1] * 100 + buffer[2] * 10 + buffer[3]);
}

